    private void bcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcariActionPerformed
        // TODO add your handling code here:
        Object[] Baris = {"Tanggal Periksa","Id Dokter","Id Pasien","Tinggi Badan","Berat Badan","Diagnosa"};
        tabmode = new DefaultTableModel(null, Baris);
        tabelrekam.setModel(tabmode);
        String sql = "select*from rekam where id_pas='"+tcari.getText()+"'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("tgl");
                String b = hasil.getString("id_dok");
                String c = hasil.getString("id_pas");
                String d = hasil.getString("tb");
                String e = hasil.getString("bb");
                String f = hasil.getString("diagnosa");
                
                String[] data={a,b,c,d,e,f};
                tabmode.addRow(data);
            }
        } catch (Exception e){
        }
    }//GEN-LAST:event_bcariActionPerformed

