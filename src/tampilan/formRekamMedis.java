    private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
        // TODO add your handling code here:
        
        String sql = "insert into rekam values (?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            Date sqlDate = new Date(dperiksa.getDate().getTime()); // Mengubah java.util.Date menjadi java.sql.Date

            stat.setDate(1, sqlDate); // Menyetel tanggal ke parameter pertama
            stat.setString(2, tid_dokter.getText());
            stat.setString(3, tid_pasien.getText());
            stat.setInt(4, Integer.parseInt(ttb.getText()));
            stat.setInt(5, Integer.parseInt(tbb.getText()));
            stat.setString(6, tdiagnosa.getText());
            stat.setString(7, tdaftar.getText());
            
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            tid_dokter.requestFocus();
            datatable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan"+e);
        }
    }//GEN-LAST:event_bsaveActionPerformed

    private void bcari_dokterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bcari_dokterActionPerformed
        // TODO add your handling code here:
        
        String sql = "select*from dokter where id_dok = '"+tid_dokter.getText()+"'";
        try{
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String nm = hasil.getString("nama_dok");
                tnama_dokter.setText(nm);
                tnama_dokter.setEnabled(false);
            }
        }catch (Exception e){}
    }//GEN-LAST:event_bcari_dokterActionPerformed

